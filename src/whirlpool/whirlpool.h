typedef uint8_t	m8x8_uint8_t[64];
typedef uint8_t	m16x16_uint8_t[256];

/**
 * Do `out = a`
 */
void	m8x8_copy(m8x8_uint8_t out, const m8x8_uint8_t a);

/**
 * Do `out_a ⊕= b`
 */
void	m8x8_xor(m8x8_uint8_t out_a, const m8x8_uint8_t b);

/**
 * Initialize `out` with `chunk` such as:
 * `out_{i, j} ≡ chunk[8i + j]`, `0 <= (i, j) <= 7`
 */
void	m8x8_init(m8x8_uint8_t out, const uint8_t chunk[64]);

/**
 * Do `out_cr = c^r`
 *
 * @param out_cr	The out matrix (futur `c^r`).
 * @param r 		The round `r`
 *
 * @note [3.6] `c^{r}_{0, j} ≡ S[8(r − 1) + j]`, `0 <= j <= 7`
 * @note [3.6] `c^{r}_{i, j} ≡ 0`, `0 <= (i, j) <= 7`
 */
void	whirlpool_round_constants(m8x8_uint8_t out_cr, const uint32_t r,
			const m16x16_uint8_t S);

/**
 * Do `out = γ(out)`
 *
 * @param out 	The input matrix. It gets updated.
 *
 * @note [3.2] `γ(a) = b` ⇔ `b_{i, j} = S[a_{i, j}]`, `0 <= (i, j) <= 7.`
 */
void	whirlpool_substitution(m8x8_uint8_t out, const m16x16_uint8_t S);

/**
 * Do `out = π(out)`
 *
 * @param out 	The input matrix. It gets updated.
 *
 * @note [3.3] `π(a) = b` ⇔ `b_{i, j} = a_{(i − j) mod 8, j}`, `0 <= (i, j) <= 7`
 */
void	whirlpool_cyclical_permutation(m8x8_uint8_t out);

/**
 * Do `out = θ(out)`
 *
 * @param out 	The input matrix. It gets updated.
 *
 * @note [3.4] `θ(a) = b` ⇔ `b = a · C`
 */
void	whirlpool_linear_diffusion(m8x8_uint8_t out, const m8x8_uint8_t C);

/**
 * Do `out = ρ[k](out)`
 *
 * @param out 	The input matrix. It gets updated.
 * @param key 	`k`
 *
 * @note [3.7] `ρ[k] ≡ σ[k] ◦ θ ◦ π ◦ γ`
 */
void	whirlpool_round(m8x8_uint8_t out, m8x8_uint8_t key,
			const m16x16_uint8_t S, const m8x8_uint8_t C);

/**
 * @brief Calculate cipher, process compression, and update the key directly
 *
 * @param chunk 	`η_i`
 * @param out_key 	`H_{i−1}` (at the end of the function, it gets updated to `H_i`)
 *
 * @note [3.11] `H_i = W[H_{i−1}](η_i) ⊕ H_{i−1} ⊕ η_i`, `i > 0`
 * @note [3.9] `W[K] = (◦_{1}^{r=R} ρ[K^r]) ◦ σ[K^0]`
 * @note [3.8] `K^0 = K`; `K^r = ρ[c^r](K^{r−1})`, `r > 0`
 */
void	whirlpool_process_chunk(const m8x8_uint8_t chunk, m8x8_uint8_t out_key,
			const m16x16_uint8_t S, const m8x8_uint8_t C);

/**
 * `a -> (μ^{−1} ◦ hexDigest)(a)`
 */
char	*whirlpool_build(const m8x8_uint8_t matrix);

/**
 * @note [3.12] `Whirlpool(M) = μ^{−1}(H_t)`
 * @note Where:
 * @note 	- `M` is a message string
 * @note 	- `μ^{−1}` is the function contructing a string from a matrix
 * @note 	- `H_t` is the output matrix of the compression function ([3.11])
 */
char	*whirlpool_hash(const char *message);

